namespace Endroid\ImportBundle;

use Endroid\ImportBundle\DependencyInjection\Compiler\ImporterCompilerPass;
use Symfony\Component\DependencyInjection\Compiler\PassConfig;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\HttpKernel\Bundle\Bundle;

class EndroidImportBundle extends Bundle
{
    /**
     * {@inheritdoc}
     */
    public function build(ContainerBuilder $container)
    {
        parent::build($container);

        $container->addCompilerPass(new ImporterCompilerPass(), PassConfig::TYPE_BEFORE_OPTIMIZATION, 1);
    }
}
